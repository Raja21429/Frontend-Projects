
//for loop stright 
for(let i=0;i<=5;i++){
    console.log(i);
} 

//for loop reverse
for(let i=5;i>=0;i--){
    console.log(i);
} 

//while loop
let i=0;
while(i<=5){
    console.log(i);
    i++;
}

//continue statement
for(let i=10;i<=100;i+=10){
    if(i==40)
    continue;
    console.log(i);
} 

//break statement
for(let i=0;i<=5;i++){
    if(i==3)
    break;
console.log(i);
}




